package Thursday_lab;



public class Person {

	int age;
    String name;
	
	
	public Person(int age, String name2) {
		super();
		this.age = age;
		this.name = name2;
	}


	public int getAge() {
		return age;
	}


	public void setAge(int age) {
		this.age = age;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}
	
	
}


